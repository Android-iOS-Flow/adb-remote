// Một số khoá hiển thị giống nhau ở mọi ngôn ngữ -> đặt ở EN, các ngôn ngữ
// khác fallback về EN nếu thiếu (vd "res.1280" = "1280 px").
const SHARED = {
  "res.1280": "1280 px", "res.1024": "1024 px", "res.720": "720 px", "res.640": "640 px",
};
Object.assign(MESSAGES.en, SHARED);

let currentLang = LS.get("lang", "") || detectLang();
const langChangeCbs = [];

function detectLang() {
  const nav = (navigator.language || "en").toLowerCase();
  for (const { code } of LANGS) {
    if (nav === code || nav.startsWith(code + "-")) return code;
  }
  return "en";
}

export function getLang() { return currentLang; }

export function onLangChange(cb) { langChangeCbs.push(cb); }

// Lấy chuỗi dịch theo khoá; thay {param} bằng giá trị truyền vào.
export function t(key, params) {
  const table = MESSAGES[currentLang] || MESSAGES.en;
  let s = table[key];
  if (s === undefined) s = MESSAGES.en[key];
  if (s === undefined) return key;            // thiếu hẳn -> trả về khoá để dễ phát hiện
  if (params) {
    s = s.replace(/\{(\w+)\}/g, (m, k) => (params[k] !== undefined ? params[k] : m));
  }
  return s;
}

// Quét DOM và áp bản dịch cho mọi phần tử có data-i18n*.
export function applyTranslations(root = document) {
  root.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.getAttribute("data-i18n"));
  });
  root.querySelectorAll("[data-i18n-ph]").forEach((el) => {
    el.setAttribute("placeholder", t(el.getAttribute("data-i18n-ph")));
  });
  root.querySelectorAll("[data-i18n-title]").forEach((el) => {
    el.setAttribute("title", t(el.getAttribute("data-i18n-title")));
  });
  document.documentElement.lang = currentLang;
  document.title = t("app.title");
}

export function setLang(code) {
  if (!MESSAGES[code]) return;
  currentLang = code;
  LS.set("lang", code);
  applyTranslations();
  for (const cb of langChangeCbs) { try { cb(code); } catch (_) {} }
}

export function initI18n() {
  applyTranslations();
}
